import random


def display_menu():
    print(
        "Welcome to the game, I hope you enjoy playing this game."
        "In this game you should fill the squares in a way that there"
        "is no repeated number in each square, in each line and in each column."
    )
    print("Select Sudoku Difficulty Level:")
    print("1. Easy")
    print("2. Medium")
    print("3. Hard")
    print("4: Exit")
    choice = input("Enter your choice (1/2/3/4): ")
    return choice


def get_random_board(level):
    if level == "1":
        return random.choice(easy_boards)
    elif level == "2":
        return random.choice(medium_boards)
    elif level == "3":
        return random.choice(hard_boards)
    elif level == "4":
        print("See you soon!")
        exit()
    else:
        print("Invalid choice! Please choose a valid difficulty level.")
        return None


def print_board(board):
    print(
        "The board so far is like this and the '.' in the table shows the empty spaces that you should put a number in each."
    )
    for i, row in enumerate(board):
        if i % 3 == 0 and i != 0:
            print("-" * 21)
        for j, num in enumerate(row):
            if j % 3 == 0 and j != 0:
                print("|", end=" ")
            print(num if num != 0 else ".", end=" ")
        print()


def main():
    choice = display_menu()
    board = get_random_board(choice)
    if board:
        print("Here is your Sudoku board:")
        print_board(board)


if __name__ == "__main__":
    main()
